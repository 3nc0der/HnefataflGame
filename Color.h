#pragma once

/// <summary>
/// An enum class for the color of the pieces and tiles.
/// </summary>
enum class Color
{
	/// <summary>
	/// The color of the white pieces and tiles.
	/// </summary>
	WHITE = 0,

	/// <summary>
	/// The color of the black pieces and tiles.
	/// </summary>
	BLACK,

	/// <summary>
	/// Used to describe no color, meaning an empty tile or no tile at all.
	/// </summary>
	NONE
};
