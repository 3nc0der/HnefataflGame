#pragma once

/// <summary>
/// An enum class for the color of the pieces.
/// </summary>
enum class Color
{
	/// <summary>
	/// The color of the white pieces.
	/// </summary>
	WHITE = 0,

	/// <summary>
	/// The color of the black pieces.
	/// </summary>
	BLACK,

	/// <summary>
	/// Used to describe no color, meaning an empty tile.
	/// </summary>
	NONE
};
